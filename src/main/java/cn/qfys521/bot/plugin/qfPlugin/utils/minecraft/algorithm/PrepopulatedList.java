    /**
     * @return aggregatedList
     */
    public static String[] getEverything() {
        Field[] fields = PrepopulatedList.class.getFields();
        for (int i = 0; i < fields.length; i++) {
            Field field = fields[i];
            if (field.getType() == String[].class) continue;
            fields[i] = null;
        }
        int size = 0;
        for (Field field : fields) {
            if (field == null) continue;
            try {
                size += ((String[]) field.get(null)).length;
            } catch (IllegalArgumentException | IllegalAccessException ignore) {
            }
        }
        String[] aggregatedList = new String[size];
        for (int i = 0, k = 0; i < fields.length; i++) {
            if (fields[i] == null) continue;
            try {
                String[] list = (String[]) fields[i].get(null);
                for (String s : list) aggregatedList[k++] = s;
            } catch (IllegalArgumentException | IllegalAccessException ignore) {
            }
        }
        return aggregatedList;
    }

